package z.homwork.tar2;

public class Box {
	
	private int length;
	private int width;
	private int height;
	
	public Box(int length, int width, int height) {
		super();
		this.length = length;
		this.width = width;
		this.height = height;
	}
	public Box(int side) {
		super();
		this.length = side;
		this.width = side;
		this.height = side;
	}
	
	

}
